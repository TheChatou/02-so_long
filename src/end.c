#include "window.h"

int	destroy_game(t_game *game)
{
	free_map(game->map);
	free_xpms(game->win.mlx, game->xpm_ptr, 14);
	free_anim(&game->good_tk, game->win.mlx);
	free_anim(&game->bad_tk, game->win.mlx);
	free_anim(&game->sta_pl, game->win.mlx);
	free_anim(&game->walk_pl, game->win.mlx);
	destroy_window(game->win);
	if (game->map.tk_pos)
		free(game->map.tk_pos);
	if (game->map.bad_pos)
		free(game->map.bad_pos);
	exit(EXIT_SUCCESS);
}

void	destroy_window(t_win w)
{
	if (w.mlx && w.win)
		mlx_destroy_window(w.mlx, w.win);
	if (w.mlx)
	{
		mlx_destroy_display(w.mlx);
		free(w.mlx);
	}
}

